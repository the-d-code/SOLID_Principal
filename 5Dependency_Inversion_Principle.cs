namespace SOLID_Principal
{
    /*
    High-level modules/classes should not depend on low-level modules/classes. 
    Both should depend upon abstractions.Abstractions should not depend upon details. 
     
    Details should depend upon abstractions.The above lines simply state that if a high module or class will be dependent more on low-level modules or 
    class then your code would have tight coupling and if you will try to make a change in one class it can break another class which is risky at the production level. 
    So always try to make classes loosely coupled as much as you can and you can achieve this through abstraction. 
    The main motive of this principle is decoupling the dependencies so if class A changes the class B doesn’t need to care or know about the changes. 
     * */

  


}
